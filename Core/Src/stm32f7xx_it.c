/* USER CODE BEGIN 0 */
void CountDown(int *playerMinute, int *playerSecond)
{
	(*playerSecond)--;
	  if(*playerSecond == -1)
	  {
		  if(*playerMinute == 0)
		  {
			  *playerSecond = 0;
			  state = 3;
		  }
		  if(*playerMinute != 0)
		  {
			  *playerSecond = 59;
			  (*playerMinute)--;
		  }
	  }
}
